class Product { 
    constructor(id, name, price, screen, back, front, img, desc, type) { 
        this.id = id;
        this.name = name;
        this.price = price;
        this.screen = screen;
        this.back = back;
        this.front = front;
        this.img = img;
        this.desc = desc;
        this.type = type;
    }
}

export default Product;